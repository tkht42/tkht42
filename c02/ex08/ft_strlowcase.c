char	*ft_strlowcase(char *str)
{
	char	*rest;

	rest = str;
	while (*str)
	{
		if ('A' <= *str && *str <= 'Z')
		{
			*str += 32;
		}
		str++;
	}
	str = rest;
	return (str);
}
