int	ft_sqrt(int nb)
{
	int	l;

	l = 0;
	if (nb <= 0)
		return (0);
	while (nb != 0)
	{
		nb = nb - (2 * l + 1);
		if (nb < 0)
			return (0);
		l++;
	}
	return (l);
}

