char	*ft_strupcase(char *str)
{
	char	*rest;

	rest = str;
	while (*str)
	{
		if ('a' <= *str && *str <= 'z')
		{
			*str -= 32;
		}
		str++;
	}
	str = rest;
	return (str);
}
